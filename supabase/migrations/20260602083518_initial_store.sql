create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.admin_users
    where user_id = auth.uid()
  );
$$;

create or replace function public.create_storefront_order(
  p_customer_name text,
  p_customer_email text,
  p_customer_phone text,
  p_items jsonb
)
returns table (
  order_id uuid,
  order_number text,
  total numeric
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_customer_id uuid;
  v_order_id uuid;
  v_order_number text;
  v_total numeric(12, 2) := 0;
  v_item jsonb;
  v_product public.products%rowtype;
  v_product_id bigint;
  v_quantity integer;
begin
  if length(trim(coalesce(p_customer_name, ''))) < 2 then
    raise exception 'Invalid customer name';
  end if;

  if length(trim(coalesce(p_customer_email, ''))) < 3 then
    raise exception 'Invalid customer email';
  end if;

  if jsonb_typeof(p_items) <> 'array' then
    raise exception 'Items must be an array';
  end if;

  if jsonb_array_length(p_items) < 1 or jsonb_array_length(p_items) > 50 then
    raise exception 'Invalid item count';
  end if;

  insert into public.customers (email, full_name, phone)
  values (
    lower(trim(p_customer_email)),
    trim(p_customer_name),
    nullif(trim(coalesce(p_customer_phone, '')), '')
  )
  on conflict (email) do update
  set
    full_name = excluded.full_name,
    phone = coalesce(excluded.phone, public.customers.phone)
  returning id into v_customer_id;

  v_order_number := '#LY' || lpad(nextval('public.order_number_seq')::text, 4, '0');

  insert into public.orders (order_number, customer_id)
  values (v_order_number, v_customer_id)
  returning id into v_order_id;

  for v_item in select value from jsonb_array_elements(p_items)
  loop
    if coalesce(v_item ->> 'productId', '') !~ '^[0-9]+$'
      or coalesce(v_item ->> 'quantity', '') !~ '^[0-9]+$' then
      raise exception 'Invalid order item';
    end if;

    v_product_id := (v_item ->> 'productId')::bigint;
    v_quantity := (v_item ->> 'quantity')::integer;

    if v_quantity < 1 or v_quantity > 99 then
      raise exception 'Invalid quantity';
    end if;

    select *
    into v_product
    from public.products
    where id = v_product_id and status = 'active'
    for update;

    if not found then
      raise exception 'Product is unavailable';
    end if;

    if v_product.stock < v_quantity then
      raise exception 'Insufficient inventory';
    end if;

    update public.products
    set stock = stock - v_quantity
    where id = v_product.id;

    insert into public.order_items (order_id, product_id, product_name, unit_price, quantity)
    values (v_order_id, v_product.id, v_product.name, v_product.price, v_quantity);

    v_total := v_total + (v_product.price * v_quantity);
  end loop;

  update public.orders
  set total = v_total
  where id = v_order_id;

  return query
  select o.id, o.order_number, o.total
  from public.orders o
  where o.id = v_order_id;
end;
$$;

revoke all on function public.create_storefront_order(text, text, text, jsonb) from public;
grant execute on function public.create_storefront_order(text, text, text, jsonb) to service_role;

